                Udara adalah sumber daya alam yang mempengaruhi kehidupan makhluk hidup. Berdasarkan Undang-Undang Nomor 31 Tahun 2009 tentang Meteorologi, Klimatologi dan Geofisika, kualitas udara adalah kadar unsur pencemaran udara dan/atau
                gas rumah kaca yang ada di atmosfer. Semua aktivitas manusia, baik sengaja atau tidak sengaja dapat menyebabkan pencemaran udara (Prabowo dan Muslim, 2018). Pencemaran udara adalah suatu masalah yang memiliki dampak buruk bagi
                kehidupan makhluk hidup (Nurjanah dkk., 2020). Pengertian pencemaran udara menurut Baran (2021) adalah rusaknya struktur kimia dari atmosfer akibat bercampurnya berbagai bahan kimia seperti gas, debu, dan aerosol yang
                menyebabkan penyakit bahkan kematian bagi makhluk hidup. Berdasarkan Peraturan Pemerintah Republik Indonesia Nomor 41 Tahun 1999 tentang Pengendalian Pencemaran Udara, pengertian pencemaran udara adalah masuknya atau
                dimasukkannya zat, energi, dan/atau komponen lain ke dalam udara ambien oleh kegiatan manusia, sehingga mutu udara ambien turun sampai ke tingkat tertentu yang menyebabkan udara ambien tidak dapat memenuhi fungsinya.
